#ifndef SGUI_SURFACE_H
#define SGUI_SURFACE_H
/*************************************************************************************************/
/*************************************************************************************************/
/*************************************************************************************************/
/*实现目标:
 *基础图形绘制之像素点绘制
 */
/*************************************************************************************************/
/*************************************************************************************************/
/*************************************************************************************************/
/* 画布 */
typedef struct SimpleGui_Surface {
    SGui_Pixel *Zone;   /* 绘制空间地址 */
    SGui_Coord  Width;  /* 绘制空间宽度 */
    SGui_Coord  Height; /* 绘制空间高度 */
} SGui_Surface;
/*************************************************************************************************/
/*************************************************************************************************/
/*************************************************************************************************/
/* 绘制空间布局配置(宽高布局:0;高宽布局:1;(通常不会用到后者)) */
#define SGui_SurfaceLayout      0
/*************************************************************************************************/
/*************************************************************************************************/
/*************************************************************************************************/
/* 画布绘制坐标:(X,Y)转缓冲区偏移量(程序扫描转换时均采用行列扫描) */
static inline SGui_Coord SGui_SurfaceOffset(SGui_Surface *Surface, SGui_Dot Dot)
{
#if 0
#elif (SGui_SurfaceLayout == 0)
    /* 缓冲区布局为:宽*高时(像素点一行一行存储) */
    return (Dot.X + Dot.Y * Surface->Width);
#elif (SGui_SurfaceLayout == 1)
    /* 缓冲区布局为:高*宽时(像素点一列一列存储) */
    return (Dot.X + Dot.Y * Surface->Height);
#else
#error "SGui_SurfaceLayout: Unsupported Surface Layout Way"
#endif
}
/*************************************************************************************************/
/*************************************************************************************************/
/*************************************************************************************************/
/* 画布绘制像素点(不携带剪切域检查): */
static inline bool SGui_SurfaceDrawPixel(SGui_Surface *Surface, SGui_Dot Dot, SGui_Pixel Pixel)
{
    Surface->Zone[SGui_SurfaceOffset(Surface, Dot)] = Pixel;
}
/*************************************************************************************************/
/*************************************************************************************************/
/*************************************************************************************************/
#endif
