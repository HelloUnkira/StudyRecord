/*************************************************************************************************/
/* 基本图元描述:坐标点 */
#define SGui_Coord   uint16_t
/*************************************************************************************************/
/*************************************************************************************************/
/*************************************************************************************************/
/* 基本图元描述:坐标 */
typedef struct SimpleGui_Dot {
    SGui_Coord X;    //横坐标
    SGui_Coord Y;    //纵坐标
} SGui_Dot;
/*************************************************************************************************/
/*************************************************************************************************/
/*************************************************************************************************/
/* 基本图元描述:区域 */
typedef struct SimpleGui_Area {
    SGui_Dot LU;     //左上角坐标(Left,  Up)
    SGui_Dot RB;     //右下角坐标(Right, Down)
} SGui_Area;
/*************************************************************************************************/
/*************************************************************************************************/
/*************************************************************************************************/
/* 非法坐标点值(这里随意指定一个最大最小值) */
#define SGUI_COORB_INVALID  ((SGui_Coord)(~0))
/*************************************************************************************************/
/*************************************************************************************************/
/*************************************************************************************************/
/* 坐标重置 */
#define SGui_Dot_Reset(Dot)         \
do {                                \
    (Dot).x = SGUI_COORB_INVALID;   \
    (Dot).y = SGUI_COORB_INVALID;   \
} while (0)                         \
/*************************************************************************************************/
/*************************************************************************************************/
/*************************************************************************************************/
/* 区域重置 */
#define SGUI_Area_Reset(Area)           \
do {                                    \
    (Area).LU.X = SGUI_COORB_INVALID;   \
    (Area).LU.Y = SGUI_COORB_INVALID;   \
    (Area).RB.X = SGUI_COORB_INVALID;   \
    (Area).RB.Y = SGUI_COORB_INVALID;   \
} while (0)                             \
/*************************************************************************************************/
/*************************************************************************************************/
/*************************************************************************************************/
/* 区域设置 */
#define SGUI_Area_Set(Area, LU_X, LU_Y, RB_X, RB_Y) \
do {                                                \
    (Area).LU.X = LU_X;                             \
    (Area).LU.Y = LU_Y;                             \
    (Area).RB.X = RB_X;                             \
    (Area).RB.Y = RB_Y;                             \
} while (0)                                         \
/*************************************************************************************************/
/*************************************************************************************************/
/*************************************************************************************************/
/* 坐标定义 */
#define SGui_Dot_Define(Name)   \
    SGui_Dot (Name) = {0};      \
    SGui_Dot_Reset(Name);       \
/*************************************************************************************************/
/*************************************************************************************************/
/*************************************************************************************************/
/* 区域定义 */
#define SGui_Area_Define(Name)  \
    SGui_Area (Name) = {0};     \
    SGUI_Area_Reset(Name);      \
