/*************************************************************************************************/
/*************************************************************************************************/
#endif
/*************************************************************************************************/
/*************************************************************************************************/
/*************************************************************************************************/
#define SGUI_LOG_MESSGAE(Format, ...) SGui_LogMessage(__FILE__, __LINE__, Format, ##__VA_ARGS__)
