#include <stdarg.h>
#include <stdio.h>
/*************************************************************************************************/
/*************************************************************************************************/
/*************************************************************************************************/
static inline void SGui_LogMessage(char *File, uint32_t Line, char *Format, ...)
{
    printf("[%s]", File);
    printf("[%u]", Line);
    va_list List;
    va_start(List, Format);
    vprintf(Format, List);
    va_end(List);
    printf("\n");
}
/*************************************************************************************************/
/*************************************************************************************************/
/*************************************************************************************************/
#else
/*************************************************************************************************/
/*************************************************************************************************/
/*************************************************************************************************/
static inline void SGui_LogMessage(char *File, uint32_t Line, char *Format, ...)
{
}
