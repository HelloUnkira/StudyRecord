#ifndef GAME2048_H
#define GAME2048_H
/*************************************************************************************************/
/*************************************************************************************************/
/*************************************************************************************************/
#include <stddef.h>
#include <stdint.h>
#include <stdbool.h>
/*************************************************************************************************/
/*************************************************************************************************/
/*************************************************************************************************/
typedef struct Game2048Descriptor {
    uint8_t   *Matrix;  /* 值上标矩阵:Scale*Scale,值为2**x次方 */
    uint32_t   Scale;   /* 值上标矩阵阶数 */
    uint8_t   *Buffer;  /* 内部使用:Scale */
    uint32_t (*Random)(uint32_t Number);    /* 随机数生成器,范围为[0, Number - 1] */
    uint8_t    Difficult;
    uint8_t    Status;
} Game2048;
/*************************************************************************************************/
/*************************************************************************************************/
/*************************************************************************************************/
typedef enum Game2048WayDescriptor{
    Game2048_None  = 0,  /* 占位符 */
    Game2048_Up    = 0x01,
    Game2048_Down  = 0x02,
    Game2048_Left  = 0x04,
    Game2048_Right = 0x08,
} Game2048Way;
/*************************************************************************************************/
/*************************************************************************************************/
/*************************************************************************************************/
bool Game2048_Ready(Game2048 *Instance, uint8_t Difficult);
bool Game2048_Execute(Game2048 *Instance, Game2048Way Way);
/*************************************************************************************************/
/*************************************************************************************************/
/*************************************************************************************************/
#endif
