#ifndef SGUI_WINDOW_H
#define SGUI_WINDOW_H
/*************************************************************************************************/
/*************************************************************************************************/
/*************************************************************************************************/
/* 控件派生类:窗口 */
typedef struct SimpleGui_Window {
    SGui_Widget         Widget;         /* 控件基类 */
    SGui_Container      Container;      /* 窗口管理属性:容器 */
    SGui_Canvas        *Canvas;         /* 窗口绘制属性:画布 */
                                        /* 窗口显示属性:窗口资源及绘制 */
    SGui_WidgetCallback Show;           /* 窗口进入:资源的申请 */
    SGui_WidgetCallback Hide;           /* 窗口退出:资源的回收 */
    SGui_WidgetCallback Display;        /* 窗口显示 */
                                        /* ...... */
                                        /* 继续补充:窗口特性 */
} SGui_Window;
/*************************************************************************************************/
/*************************************************************************************************/
/*************************************************************************************************/
/* 任意类型控件找寻到包容它的窗口 */
SGui_Handle SGui_WidgetGetWindow(SGui_Handle Widget_H);
/*************************************************************************************************/
/*************************************************************************************************/
/*************************************************************************************************/
/* 创建一个窗口实例(必须指定子成员最大数量) */
SGui_Handle SGui_WindowCreate(uint32_t Number);
/*************************************************************************************************/
/*************************************************************************************************/
/*************************************************************************************************/
/* 销毁一个窗口实例(只回收本体,不会递归销毁子控件) */
void SGui_WindowDestroy(SGui_Handle Window_H);
/*************************************************************************************************/
/*************************************************************************************************/
/*************************************************************************************************/






/*************************************************************************************************/
/*************************************************************************************************/
/*************************************************************************************************/
#endif
