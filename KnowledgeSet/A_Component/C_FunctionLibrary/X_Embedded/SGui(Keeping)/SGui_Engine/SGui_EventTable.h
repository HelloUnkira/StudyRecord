#ifndef SGUI_EVENT_TABLE_H
#define SGUI_EVENT_TABLE_H
/*************************************************************************************************/
/*************************************************************************************************/
/*************************************************************************************************/
/* 注入到控件中的通配事件类型表 */
typedef enum SimpleGui_EventTable {
    SGui_EventType_None = 0,    /* 空事件,占位符 */
                                /* 继续添加 */
    SGui_EventType_Animation,   /* 控件的指定动画事件 */
                                /* ... */
    SGui_EventType_All,         /* 事件类型总数 */
} SGui_EventTable;
/*************************************************************************************************/
/*************************************************************************************************/
/*************************************************************************************************/
/* 注入到系统中的通配事件队列类型表 */
typedef enum SimpleGui_EventQueueTable {
    SGui_EventQueue_None = 0,   /* 空事件队列,占位符 */
                                /* 继续添加(数字越小优先级越高,事件优先处理) */
    SGui_EventQueue_Main,       /* 主事件队列 */
                                /* ... */
    SGui_EventQueue_All,        /* 事件队列类型总数(< SGui_EventQueue_Number) */
} SGui_EventQueueTable;
/*************************************************************************************************/
/*************************************************************************************************/
/*************************************************************************************************/
#if SGui_EventQueue_All >= SGui_EventQueue_Number
#error "SGui_EventQueue_Number: event queue is too small, you need adjust this definition"
#endif
/*************************************************************************************************/
/*************************************************************************************************/
/*************************************************************************************************/
/* 注入到系统中的调配事件执行状态 */
typedef enum SimpleGui_EventStatusTable {
    SGui_EventStatus_None = 0,  /* 空状态,响应错误,占位符 */
                                /* 继续添加 */
    SGui_EventStatus_Over,      /* 事件响应结束 */
    SGui_EventStatus_Continue,  /* 事件响应未完成,继续 */
                                /* ... */
    SGui_EventStatus_All,       /* 事件响应状态总数 */
} SGui_EventStatusTable;
/*************************************************************************************************/
/*************************************************************************************************/
/*************************************************************************************************/
/* 配置事件表 */
void SGui_EventAdaptorTableConfigure(void);
/*************************************************************************************************/
/*************************************************************************************************/
/*************************************************************************************************/
#endif
