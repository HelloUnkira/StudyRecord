#ifndef SGUI_WIDGET_H
#define SGUI_WIDGET_H
/*************************************************************************************************/
/*************************************************************************************************/
/*************************************************************************************************/
/* 注入到控件中的控件类型表 */
typedef enum SimpleGui_WidgetTable {
    SGui_WidgetType_None = 0,   /* 空控件,占位符 */
                                /* 继续补充 */
    SGui_WidgetType_Window,     /* 窗口控件 */
                                /* ...... */
    SGui_WidgetType_All,        /* 控件类型种数 */
} SGui_WidgetTable;
/*************************************************************************************************/
/*************************************************************************************************/
/*************************************************************************************************/
typedef void (*SGui_WidgetCallback)(SGui_Handle Widget);
/*************************************************************************************************/
/*************************************************************************************************/
/*************************************************************************************************/
/* 控件基类(注意:它是一个最小单元类) */
typedef struct SimpleGui_Widget {
    SGui_Handle         Parent;         /* 控件的父控件:祖先控件无父控件 */
    SGui_EventCallback  Callback;       /* 事件派发回调:事件派发到指定控件 */
    union {                             /* 控件区域描述: */
        SGui_Area       Offset;         /* 父控件存在:相对父控件的偏移域 */
        SGui_Area       Clip;           /* 父控件不存在:相对画布的剪切域 */
    };
    SGui_Pixel          Pixel;          /* 控件底色描述:控件重绘时使用 */
    SGui_WidgetTable    Type;           /* 派生控件类型 */
                                        /* ...... */
                                        /* 继续补充:控件通性 */
} SGui_Widget;
/*************************************************************************************************/
/*************************************************************************************************/
/*************************************************************************************************/
#endif
