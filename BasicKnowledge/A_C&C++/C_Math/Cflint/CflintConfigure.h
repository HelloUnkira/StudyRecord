/* 辅助功能:测试阶段所用于确定的大小端序检查 */
static inline uint8_t Cflint_EndianCheck(void)
{
    union {
        uint8_t  Byte;
        uint32_t Integer;
    } Endian;
    
    Endian.Integer = 1;
    
    return (Endian.Byte == 1 ? 1 : 0);
}
/*****************************************************************************/
/*****************************************************************************/
/*****************************************************************************/
/* 辅助功能:测试阶段所用于确定的平台字节对齐检查 */
static inline uint8_t Cflint_BytesCheck(void)
{
    return sizeof(void *);
}
